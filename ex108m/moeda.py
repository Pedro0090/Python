def metade(x=0):
    res = x / 2
    return res


def dobro(x=0):
    res = x * 2
    return res


def aumentar(x=0, t=0):
    res = x + (x * t / 100)
    return res


def diminuir(x=0, t=0):
    res = x - (x * t / 100)
    return res


def moeda(x=0, m='R$'):
    return f'{m}{x:.2f}'.replace('.', ',')
